    public void updateChooseBuddyComboBox(){
        box.removeAllElements();
        for(int i = 0; i < server.buddies.size(); i++){
            box.addElement(server.buddies.get(i));
            System.out.println("Added " + server.buddies.get(i) + " to the list / box.");
        }
    }

